import Foundation
import SwiftUI

extension Color {
    static let theme = Theme()
}

struct Theme {
    let beige = Color("beige")
    let darkBeige = Color("darkBeige")
    let black = Color("black")
    let grey = Color("grey")
    let lightGrey = Color("lightGrey")
}
